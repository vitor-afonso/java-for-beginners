public class App {
    public static void main(String[] args) throws Exception {

        int result = 10 + 3 * 2; // results in 16
        System.out.println(result);

        // "()" preceds "*" and "/" that preceds "+" and "-"
        int result2 = (10 + 3) * 2; // results in 26
        
        System.out.println(result2);
    }
}
